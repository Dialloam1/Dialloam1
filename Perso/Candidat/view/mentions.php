<h3>Mentions Légales</h3>
<strong>
L’application CEFISI2022 est amenée à traiter vos données personnelles dans le cadre de vos candidatures.
Ces données sont traitées de façon confidentielle. Seules les personnels concernés par les candidatures peuvent accéder aux données à des fins strictement internes.
Les données des candidats non retenus sont supprimées à l'issue du recrutement.
